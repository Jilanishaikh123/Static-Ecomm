// Declaring footer
let footer = document.getElementById("footer");

//This section is used to display category in the drop down list
let category_selector = document.getElementById("category_selector");
for (let category in products) {
  let litag = document.createElement("li");
  category_selector.append(litag);

  let atag = document.createElement("a");
  atag.setAttribute("class", "dropdown-item");
  atag.innerText = category;
  atag.addEventListener("click", () => display_selected_category(category));
  litag.append(atag);
}
let litag = document.createElement("li");
category_selector.append(litag);

let atag = document.createElement("a");
atag.setAttribute("class", "dropdown-item");
atag.innerText = "All";
atag.addEventListener("click", () => display_selected_category("All"));
litag.append(atag);

//function to display products of selected category
function display_selected_category(category_name) {
  if (document.querySelector(".container")) {
    document.querySelector(".container").remove();
  }
  if (category_name == "All") {
    display_products();
  } else {
    let container = document.createElement("div");
    container.setAttribute("class", "container");

    let row = document.createElement("div");
    row.setAttribute("class", "row");
    row.setAttribute("id", "product_row");

    container.append(row);
    let category_heading = document.createElement("h1");
    category_heading.setAttribute("class", "category_heading");
    category_heading.textContent = category_name;
    row.append(category_heading);

    products[category_name].forEach((element) => {
      let column = document.createElement("div");
      column.setAttribute("class", "col-4");
      column.setAttribute("id", "product_col");
      row.append(column);
      let card = document.createElement("div");
      card.setAttribute("class", "card");
      card.style.width = "18rem";
      card.style.height = "28rem";
      column.append(card);

      let img_container = document.createElement("div");
      img_container.setAttribute("class", "img_container");
      card.append(img_container);

      let product_img = document.createElement("img");
      product_img.setAttribute("class", "product_img");
      product_img.setAttribute("src", element.p_image);
      img_container.append(product_img);

      let card_body = document.createElement("div");
      card_body.setAttribute("class", "card-body");
      card.append(card_body);
      let card_title = document.createElement("h5");
      card_title.setAttribute("class", "card-title");
      card_title.textContent = element.name;
      card_body.append(card_title);
      let card_txt = document.createElement("p");
      card_txt.setAttribute("class", "card-text");
      card_txt.setAttribute("id", "card_txt");
      card_txt.textContent = element.description;
      card_body.append(card_txt);
      let price = document.createElement("p");
      price.setAttribute("class", "card-text");
      price.textContent = `${element.price} Rs.`;
      card_body.append(price);
      let add_to_cart = document.createElement("a");
      add_to_cart.setAttribute("class", "btn btn-primary");
      add_to_cart.textContent = "Add to cart";
      card_body.append(add_to_cart);
    });

    document.body.insertBefore(container, footer);
  }
}

